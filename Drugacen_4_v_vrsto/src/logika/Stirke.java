package logika;

public class Stirke {
	
	/* Stirke so vse možne zmagovalne kombinacije. 
	 * Predstavljene so z dvema tabelama dolzine 4. Prva tabela predstavlja x in druga y koordinate. */
	
	public int[] x;
	public int[] y;
	
	public Stirke(int[] x, int[] y) {
		super();
		this.x = x;
		this.y = y;
	}
	
	

}
